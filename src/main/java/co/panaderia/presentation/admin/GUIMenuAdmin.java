        resetColor(BtnHomePage);
        setColor(BtnProductos);
        resetColor(BtnProveedores);
        resetColor(BtnInsumos);
        resetColor(BtnClientes);
        resetColor(BtnEmpleados);
        resetColor(BtnContabilidad);

